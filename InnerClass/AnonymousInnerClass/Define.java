package InnerClass.AnonymousInnerClass;

public class Define {
    /*
    匿名内部类是内部类的简化写法。本质是一个带具体实现的父类或父接口的匿名的子类对象。
    最常用到的内部类就是匿名内部类。
    以接口举例，使用一个接口时，需要：
        1.定义子类
        2.重写接口中的方法
        3.创建子类对象
        4.调用重写后的方法
    最终的目的只是为了调用方法，匿名内部类就是把以上四步合成一步。

    前提：匿名内部类必须继承一个父类或实现一个父接口

    格式：
        new 父类名或接口名() {
            // 方法重写
            @Override
            public void method() {
                // 执行语句
            }
        };
     */
}
